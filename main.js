//Operador igualdad
let a = 50;
let b = 100;

console.log(`Operador igualdad ${a == b}`);

//Operador diferencia
let c = 20;
let d = 100;

console.log(`Operador diferencia ${c != d}`);

//Operador identica
let e = '100';
let f = '100';

console.log(`Tipo de dato variable e ${typeof(e)} valor del dato ${e}`);
console.log(`Tipo de dato variable f ${typeof(f)} valor del dato ${f}`);
console.warn(`Resultado de la comparacion ${e === f}`);

//Operador identica
let g = '100';
let h = 50;

console.log(`Tipo de dato variable g ${typeof(g)} valor del dato ${g}`);
console.log(`Tipo de dato variable h ${typeof(h)} valor del dato ${h}`);
console.warn(`Resultado de la comparacion ${g !== h}`);

